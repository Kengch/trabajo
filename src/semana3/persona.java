public class persona {
    private String nombre;
    private int cedula;
    
    //constructor
    public persona(){
        this.nombre = "";
        this.cedula = 0;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }
}
